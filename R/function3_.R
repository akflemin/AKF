#' Function3_Sum of two terms
#'
#' @param x (any number)
#' @param y (any number)
#'
#' @return
#' @export
#'
#' @examples
sums <- function(x,y) {x + y}

#' Subtract two DF or numbers
#'
#' @param n (can be a df or number)
#' @param y
#'
#' @return
#' @export
#'
#' @examples
sub <-function(n,y) {sum(n)-sum(y)}
